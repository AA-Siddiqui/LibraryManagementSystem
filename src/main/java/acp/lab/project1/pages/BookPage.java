    private void preInit(String BID) {
        authors = "<html><p style='textAlign:center'>";
        categories = "";
        Connection con = null;
        try {
            con = ConnectionManager.getConnection();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        if (con == null) return;
        
        try {
            ResultSet rs = con.createStatement().executeQuery("select * from Book where BookId = " + BID);
            while (rs.next()) {
                URL = rs.getString("ThumbnailURL");
                ISBN = rs.getString("ISBN");
                title = rs.getString("Title");
                description = rs.getString("Description");
                yearPublished = rs.getInt("YearPublished");
                rating = rs.getFloat("Rating");
                pages = rs.getInt("Pages");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        try {
            ResultSet rs = con.createStatement().executeQuery("select FullName from Author where BookId = " + BID);
            while (rs.next()) {
                authors += rs.getString("FullName") + "<br>";
            }
            authors = authors.substring(0, authors.length() - 4) + "</p></html>";
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        try {
            ResultSet rs = con.createStatement().executeQuery("select FullName from Category where BookId = " + BID);
            while (rs.next()) {
                categories += rs.getString("FullName") + ", ";
            }
            categories = categories.substring(0, categories.length() - 2);
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        if (rating > 0) return;
        try {
            ResultSet rs = con.createStatement().executeQuery("select avg(Rating) as r from Rating where BookId = " + BID);
            while (rs.next()) {
                rating = rs.getFloat("FullName");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

