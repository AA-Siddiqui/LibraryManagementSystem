    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < LoggedInUser.cart.size(); i++) {
            if (LoggedInUser.cart.get(i) == bookID) {
                LoggedInUser.cart.remove(i);
                javax.swing.JOptionPane.showMessageDialog(this, "Item removed from cart!", 
                                   "Info", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        
        java.text.SimpleDateFormat formatter = new java.text.SimpleDateFormat("yyyy-MM-dd");
        java.util.Date refDate = null;
        try {
            refDate = formatter.parse("1999-12-31");
        } catch (java.text.ParseException e) {
            e.printStackTrace();
        }
        
        for (int i = 0; i < LoggedInUser.booksBorrowed.size(); i++) {
            if (LoggedInUser.booksBorrowed.get(i).ID == bookID && LoggedInUser.booksBorrowed.get(i).returnDate.compareTo(refDate) == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "You already have this book borrowed!", 
                                   "ERROR", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        if (LoggedInUser.cart.size() + LoggedInUser.borrowedBookCount == (LoggedInUser.userType == 1 ? 3 : 5)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Number of items in cart and already borrowed exceeds your limit!", 
                                   "ERROR", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        LoggedInUser.cart.add(bookID);
        javax.swing.JOptionPane.showMessageDialog(this, "Item added to cart!", 
                                   "Info", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton5ActionPerformed

