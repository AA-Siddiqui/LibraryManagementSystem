    private void upgradeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upgradeButtonActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        try {
            con = ConnectionManager.getConnection();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        if (con == null) return;
        
        int inserted = 0;
        for (int i = 0; i < LoggedInUser.cart.size(); i++) {
            try {
                inserted += con.createStatement().executeUpdate("insert into Record(UserId, BookId) values (" + LoggedInUser.userID + ", " + LoggedInUser.cart.get(i) + ");");
                System.out.println(Integer.valueOf(inserted).toString() + " rows inserted!");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        javax.swing.JOptionPane.showMessageDialog(this, Integer.valueOf(inserted).toString() + " books borrwed!", 
                                       "Info", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        LoggedInUser.update(LoggedInUser.userID);
        DashboardPage.main(new String[]{});
        setVisible(false);
        dispose();
    }//GEN-LAST:event_upgradeButtonActionPerformed

