    private void preInit() {
        Connection con = null;
        try {
            con = ConnectionManager.getConnection();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        if (con == null) return;
        
        for (int i = 0; i < LoggedInUser.cart.size(); i++) {
            try {
                ResultSet rs = con.createStatement().executeQuery("select Title from Book where BookId = " + LoggedInUser.cart.get(i));
                while (rs.next()) {
                    javax.swing.JLabel temp = new javax.swing.JLabel();

                    temp.setFont(new java.awt.Font("Roboto Condensed", 0, 18)); // NOI18N

                    temp.setForeground(DefinedColors.whiteColor);

                    temp.setText(rs.getString("Title"));

                    java.awt.GridBagConstraints gridBagConstraints = new java.awt.GridBagConstraints();
                    gridBagConstraints.gridx = 0;
                    gridBagConstraints.gridy = i;
                    jPanel5.add(temp, gridBagConstraints);
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }

